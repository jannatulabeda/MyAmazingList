import UIKit

extension UIColor {
    class func _indicatorBackgroundColor() -> UIColor {
        return UIColor.init(red: 209.0 / 255.0, green: 209.0 / 255.0, blue: 209.0 / 255.0, alpha: 1.0)
    }
}
